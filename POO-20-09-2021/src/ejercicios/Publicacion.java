package ejercicios;

public class Publicacion {

	//atributos
	public String editor;
	public String fecha;
	
	//constructor
	public Publicacion() {
		
	}
	
	public String getEditor() {
		return editor;
	}
	public void setEditor(String editor) {
		this.editor = editor;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
	//metodos
	
	
}
